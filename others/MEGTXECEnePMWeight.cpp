//______________________________________________________________________________
std::vector<Int_t> MEGTXECEnePMWeight::SelectPMRejected()
{
   const Int_t nPM = gAnalyzer->GetXECRunHeader()->GetNPM();
   std::vector<Int_t> vPMIndex;
   // reject using timefit result
   if (gAnalyzer->GetXECTimeFitResultSize() >= 1) {
      for (Int_t iPM = 0; iPM < gAnalyzer->GetXECTimeFitResultAt(0)->GetvalidSize(); iPM++) {
         if (!gAnalyzer->GetXECTimeFitResultAt(0)->GetvalidAt(iPM)) {
            vPMIndex.push_back(iPM);
         }
      }
   }

   //reject channels around pileup
   const Float_t kEliminationRange = 20.94 * MEG::degree;//from MEG
   TVector3 plPos, pmPos;
   Double_t xyz[3];
   for (auto gamma : fvPLGamma) {
      plPos.SetXYZ(XECTOOLS::UVW2X(gamma->fUVW[0], gamma->fUVW[1], 0),
                   XECTOOLS::UVW2Y(gamma->fUVW[0], gamma->fUVW[1], 0),
                   XECTOOLS::UVW2Z(gamma->fUVW[0], gamma->fUVW[1], 0));

      for (Int_t iPM = 0; iPM < nPM; iPM++) {
         xyz[0] = gAnalyzer->GetXECPMRunHeaderAt(iPM)->GetXYZAt(0);
         xyz[1] = gAnalyzer->GetXECPMRunHeaderAt(iPM)->GetXYZAt(1);
         xyz[2] = gAnalyzer->GetXECPMRunHeaderAt(iPM)->GetXYZAt(2);
         pmPos.SetXYZ(xyz[0], xyz[1], xyz[2]);
         if (plPos.Angle(pmPos) * TMath::RadToDeg() < kEliminationRange) {
            vPMIndex.push_back(iPM);
         }
      }
   }

   //remove overlap
   std::sort(vPMIndex.begin(), vPMIndex.end());
   vPMIndex.erase(std::unique(vPMIndex.begin(), vPMIndex.end()), vPMIndex.end());

   return vPMIndex;
}

//______________________________________________________________________________
void MEGTXECEnePMWeight::CalcWeight(RecGamma* gamma)
{

   for (auto pm : gamma->fvPM) {
      pm->fWeight = 1.;
      pm->fWeight *= pm->fPMRH->GetQsum2Weight();
      // pm->fWeight *= pm->fCharge2Npho;
      pm->fWeight *= pm->fSolidAngleCorr;
      pm->fWeight *= pm->fFaceCorr;
      pm->fWeight *= pm->fDeadCorr;
   }

}

//______________________________________________________________________________
Bool_t MEGTXECEnePMWeight::GetPosition(Int_t iGamma, RecGamma* gamma)
{
   if (gamma->fPosFlag) {
      return true;
   }

   std::array<Float_t, 3> xyz, uvw;

   const Int_t posType = GetSP()->GetPositionType();
   switch (posType) {
   case kPosMCTruth:
      if (gAnalyzer->GetMCXECHitSize() > iGamma) {
         MEGMCXECHit* mcxechit = gAnalyzer->GetMCXECHitAt(iGamma);//<--could be consistent when iGamma > 0?
         xyz[0] = mcxechit->Getxyz1ConvAt(0);
         xyz[1] = mcxechit->Getxyz1ConvAt(1);
         xyz[2] = mcxechit->Getxyz1ConvAt(2);
         uvw[0] = XECTOOLS::XYZ2U(xyz[0], xyz[1], xyz[2]);
         uvw[1] = XECTOOLS::XYZ2V(xyz[0], xyz[1], xyz[2]);
         uvw[2] = XECTOOLS::XYZ2W(xyz[0], xyz[1], xyz[2]);
      } else {
         Report(R_WARNING, "Position cannot be read from MC.\n");
         return false;
      }
      break;
   case kPosMean:
      if (gAnalyzer->GetXECPosLocalFitResultSize() > iGamma) {
         uvw[0] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwmeanAt(0);
         uvw[1] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwmeanAt(1);
         uvw[2] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwmeanAt(2);
         xyz[0] = XECTOOLS::UVW2X(uvw[0], uvw[1], uvw[2]);
         xyz[1] = XECTOOLS::UVW2Y(uvw[0], uvw[1], uvw[2]);
         xyz[2] = XECTOOLS::UVW2Z(uvw[0], uvw[1], uvw[2]);
      } else {
         Report(R_WARNING, "Position cannot be read from PosLocalFitResult.\n");
         return false;
      }
      break;
   case kPosLocalFit:
      if (gAnalyzer->GetXECPosLocalFitResultSize() > iGamma) {
         uvw[0] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwAt(0);
         uvw[1] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwAt(1);
         uvw[2] = gAnalyzer->GetXECPosLocalFitResultAt(iGamma)->GetuvwAt(2);
         xyz[0] = XECTOOLS::UVW2X(uvw[0], uvw[1], uvw[2]);
         xyz[1] = XECTOOLS::UVW2Y(uvw[0], uvw[1], uvw[2]);
         xyz[2] = XECTOOLS::UVW2Z(uvw[0], uvw[1], uvw[2]);
      } else {
         Report(R_WARNING, "Position cannot be read from PosLocalFitResult.\n");
         return false;
      }
      break;
   default:
      Report(R_WARNING, "Position type %d is unknown. PosLocalFit is used instead.\n", posType);
      GetSP()->SetPositionType(kPosLocalFit);
      return GetPosition(iGamma, gamma);
      break;
   }

   gamma->fUVW = uvw;

   if (uvw[0] == XECTOOLS::kXECInvalidU ||
       uvw[1] == XECTOOLS::kXECInvalidV ||
       uvw[2] == XECTOOLS::kXECInvalidW) {
      // Invalid values directly filled instead of XECTOOLS::UVW2XYZ(invalid uvw).
      xyz[0] = XECTOOLS::kXECInvalidX;
      xyz[1] = XECTOOLS::kXECInvalidY;
      xyz[2] = XECTOOLS::kXECInvalidZ;
   }
   gamma->fXYZ = xyz;
   gamma->fPosFlag = true;

   return true;
}

