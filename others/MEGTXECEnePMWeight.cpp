void MEGTXECEnePMWeight::GetSolidAngleCorrFunc()
{
   MEGXECEnePMWeightCorrection *pCorrection = gAnalyzer->GetXECEnePMWeightCorrection();

   TString name = "fXECEnePMWeightSolidAngleCorrFunc";
   TString nameCoeff = "fXECEnePMWeightSolidAngleCorrFuncCoeff";
   TString formula;
   const Int_t nPar = pCorrection->GetSolidAngleCorrNPar();

   switch (pCorrection->GetSolidAngleCorrType()) {
   case kSolidAngleNoCorrection :
   default :
      break;
   case kSolidAngleCorrWithPol :
      formula.Form("pol%d", nPar);
      fSolidAngleCorrFunc = new TF1(name, formula, 0, 6);
      for (Int_t iPar = 0; iPar < nPar; iPar++) {
         fSolidAngleCorrFunc->SetParameter(iPar, pCorrection->GetSolidAngleCorrParAt(iPar));
      }

      fSolidAngleCorrFuncNorm = fSolidAngleCorrFunc->Eval(pCorrection->GetSolidAngleCorrNormAngle());
      break;
   case kSolidAngleCorrWithLog :
      fSolidAngleCorrFuncCoeff = new TF1(nameCoeff, "pol1", -5, 5);
      fSolidAngleCorrFuncCoeff->SetParameter(0, pCorrection->GetSolidAngleCorrParAt(0));
      fSolidAngleCorrFuncCoeff->SetParameter(1, pCorrection->GetSolidAngleCorrParAt(1));

      fSolidAngleCorrFunc = new TF1(name, "[0] + [1] * TMath::Log(x)", 0, 6);
      fSolidAngleCorrFunc->SetParameter(0, pCorrection->GetSolidAngleCorrParAt(2));
      fSolidAngleCorrFunc->SetParameter(1, fSolidAngleCorrFuncCoeff->Eval(pCorrection->GetSolidAngleCorrDepthBoundary()));

      fSolidAngleCorrFuncNorm = fSolidAngleCorrFunc->Eval(pCorrection->GetSolidAngleCorrNormAngle());
      break;
   }
}

