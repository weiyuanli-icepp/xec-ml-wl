//______________________________________________________________________________
void MEGTXECEnePMWeight::RecoverDeadChannelFromTemplate(RecGamma* gamma)
{
   if (fNphoEstimation->UpdateInfo(gAnalyzer->GetCurrentRunNumber(), gAnalyzer->GetCurrentEventNumber())) {
      for (auto pm : gamma->fvPM) {
         fNphoEstimation->UpdatePM(pm->fIndex, pm->fNpho);
      }
   }

   std::vector<Int_t> vPMRejected = SelectPMRejected();
   if (!fNphoEstimation->EnergyFit(gamma->fXYZ, gamma->fEnergy, vPMRejected, true)) {
      Report(R_WARNING, "Energy fitting is invalid. Dead channel recovery is skipped.");
      return;
   }
   Float_t energy = fNphoEstimation->GetEnergyEstimated();

   std::map<Int_t, std::pair<Float_t, Float_t> > vPM;
   std::array<Float_t, 3> xyzSym, uvwSym;
   for (auto pm : gamma->fvPM) {
      if (pm->fInvalid) {
         vPM = fNphoEstimation->GetPMAtSymmetricPosition(pm->fIndex, pm->fPMRH);

         Int_t pmIndex(-1), nAdd(0);
         Float_t npho(0), nphoUncert(0), nphoAve(0);
         std::pair<Float_t, Float_t> sign;
         for (auto itr = vPM.begin(); itr != vPM.end(); itr++) {
            pmIndex = itr->first;
            sign = itr->second;
            PM* pm2 = gamma->FindPM(pmIndex);
            if (!pm2 || pm2->fInvalid) {
               continue;
            }

            xyzSym[0] = gamma->fXYZ[0];
            xyzSym[1] = sign.second * gamma->fXYZ[1];
            xyzSym[2] = sign.first  * gamma->fXYZ[2];
            uvwSym[0] = sign.first  * gamma->fUVW[0];
            uvwSym[1] = sign.second * gamma->fUVW[1];
            uvwSym[2] = gamma->fUVW[2];

            if (fNphoEstimation->EstimateNpho(pmIndex, xyzSym, uvwSym, energy, npho, nphoUncert)) {
               nphoAve += npho;
               nAdd++;
            }
         }

         if (nAdd) {
            nphoAve /= nAdd;
         }
         fNphoEstimation->LimitReplacement(pm->fIndex, pm->fPMRH->GetFace(), nphoAve);
      }
   }
   //copy to gamma
   for (auto pm : gamma->fvPM) {
      pm->fNphoEstimated = fNphoEstimation->GetNphoEstimatedAt(pm->fIndex);
      pm->fNphoEstimatedFlag = fNphoEstimation->GetNphoEstimatedFlagAt(pm->fIndex);
      pm->fNphoFit = fNphoEstimation->GetNphoFitAt(pm->fIndex);
   }

   Float_t nsum2Raw = 0;
   Float_t nsum2Recovered = 0;
   Float_t factor;
   for (auto pm : gamma->fvPM) {
      factor = pm->fCharge2Npho * pm->fSolidAngleCorr * pm->fFaceCorr;
      nsum2Raw += pm->fNpho * factor;
      nsum2Recovered += (pm->fNphoEstimatedFlag ? pm->fNphoEstimated : pm->fNpho) * factor;
   }
   for (auto pm : gamma->fvPM) {
      pm->fDeadCorr = nsum2Recovered / nsum2Raw;//constant enhancement
   }

}

//______________________________________________________________________________
void MEGTXECEnePMWeight::RecoverDeadChannelFromMLInpainting(RecGamma* gamma)
{
#ifdef HAVE_LIBTORCH
   if (!fInpainterModel) return;
   auto* model = static_cast<torch::jit::Module*>(fInpainterModel);

   const Int_t kNSensors = 4760;

   // 1. Build input (1, 4760, 2) with raw npho + relative time, and mask (1, 4760)
   // Time input is tpm relative to the earliest tpm among channels with nphe > 50,

   // Find min tpm over valid channels with nphe > 50
   Float_t minTpm = std::numeric_limits<Float_t>::max();
   for (auto pm : gamma->fvPM) {
      if (pm->fInvalid || pm->fPMRH->GetIsBad()) continue;
      auto* clus = gAnalyzer->GetXECPMClusterAt(pm->fIndex);
      if (gamma->fIndex >= clus->GettpmSize()) continue;
      Float_t nphe = gamma->fIndex < clus->GetnpheSize() ? clus->GetnpheAt(gamma->fIndex) : 0.f;
      Float_t tpm  = clus->GettpmAt(gamma->fIndex);
      if (nphe > 50.f && std::isfinite(tpm) && tpm < minTpm) minTpm = tpm;
   }
   if (minTpm == std::numeric_limits<Float_t>::max()) minTpm = 0.f;

   auto input = torch::zeros({1, kNSensors, 2});
   // auto mask = torch::zeros({1, kNSensors});
   auto mask = torch::ones({1, kNSensors});
   auto in_a = input.accessor<float, 3>();
   auto mk_a = mask.accessor<float, 2>();

   for (auto pm : gamma->fvPM) {
      Int_t i = pm->fIndex;
      if (i < 0 || i >= kNSensors) continue;

      if (pm->fInvalid || pm->fPMRH->GetIsBad()) {
         // mk_a[0][i] = 1.0f;  // masked
         // For time: average relative tpm of surrounding valid channels on same face
         Float_t tSum = 0; Int_t nV = 0;
         Float_t dThr = gAnalyzer->GetXECEnePMWeightCorrection()
                            ->GetDeadChannelRecoveryDistanceThreshold();
         for (auto jpm : gamma->fvPM) {
            if (jpm->fInvalid || jpm->fPMRH->GetIsBad()) continue;
            if (jpm->fPMRH->GetFace() != pm->fPMRH->GetFace()) continue;
            Double_t d = TMath::Sqrt(
               TMath::Power(pm->fPMRH->GetXYZAt(0)-jpm->fPMRH->GetXYZAt(0),2)+
               TMath::Power(pm->fPMRH->GetXYZAt(1)-jpm->fPMRH->GetXYZAt(1),2)+
               TMath::Power(pm->fPMRH->GetXYZAt(2)-jpm->fPMRH->GetXYZAt(2),2));
            if (d < dThr) {
               auto* jclus = gAnalyzer->GetXECPMClusterAt(jpm->fIndex);
               if (gamma->fIndex < jclus->GettpmSize()) {
                  tSum += jclus->GettpmAt(gamma->fIndex) - minTpm;
                  nV++;
               }
            }
         }
         in_a[0][i][0] = 0.f;  // npho placeholder (model uses sentinel via mask)
         in_a[0][i][1] = nV > 0 ? tSum / nV : 0.f;
      } else {
         mk_a[0][i] = 0.0f;
         in_a[0][i][0] = pm->fNpho;
         auto* clus = gAnalyzer->GetXECPMClusterAt(i);
         Float_t tpm = gamma->fIndex < clus->GettpmSize()
                       ? clus->GettpmAt(gamma->fIndex) : 0.f;
         in_a[0][i][1] = tpm - minTpm;
      }
   }
   // Sensors beyond nPM: mark as masked
   for (Int_t i = (Int_t)gamma->fvPM.size(); i < kNSensors; i++)
      mk_a[0][i] = 1.0f;

   // 2. Inference (model handles normalization internally)
   torch::NoGradGuard no_grad;
   auto output = model->forward({input, mask}).toTensor();  // (1, 4760)
   auto out_a = output.accessor<float, 2>();

   // 3. Set fNphoEstimated for dead channels
   for (auto pm : gamma->fvPM) {
      if (pm->fInvalid || pm->fPMRH->GetIsBad()) {
         Float_t npho = out_a[0][pm->fIndex];
         pm->fNphoEstimated = npho > 0 ? npho : 0;
         pm->fNphoEstimatedFlag = true;
      }
   }

   // 4. Compute fDeadCorr per face
   Float_t nface2Raw[XECCONSTANTS::kNumberOfFaces] = {0.};
   Float_t nface2Rec[XECCONSTANTS::kNumberOfFaces] = {0.};
   for (auto pm : gamma->fvPM) {
      Float_t f = pm->fPMRH->GetQsum2Weight() * pm->fSolidAngleCorr * pm->fFaceCorr;
      if (pm->fNpho >= 0) {
         nface2Raw[pm->fPMRH->GetFace()] += pm->fNpho * f;
      }
      if (pm->fNphoEstimatedFlag) {
         if (pm->fNphoEstimated > 0) {
            nface2Rec[pm->fPMRH->GetFace()] += pm->fNphoEstimated * f;
         }
      } else {
         if (pm->fNpho >= 0) {
            nface2Rec[pm->fPMRH->GetFace()] += pm->fNpho * f;
         }
      }
   }
   for (auto pm : gamma->fvPM) {
      if (nface2Raw[pm->fPMRH->GetFace()] > 0 && nface2Rec[pm->fPMRH->GetFace()] > 0) {
         pm->fDeadCorr = nface2Rec[pm->fPMRH->GetFace()] / nface2Raw[pm->fPMRH->GetFace()];
      } else {
         pm->fDeadCorr = 1.;
      }
   }
#else
   Report(R_ERROR, "ML inpainting (method 4) requires LibTorch. Rebuild with LIBTORCHSYS.");
#endif
}

//______________________________________________________________________________
void MEGTXECEnePMWeight::RecoverDeadChannelFromSurroundings(RecGamma* gamma)
{
   // Check gamma position
   if (gamma->fXYZ[0] == XECTOOLS::kXECInvalidX
       || gamma->fXYZ[1] == XECTOOLS::kXECInvalidY
       || gamma->fXYZ[2] == XECTOOLS::kXECInvalidZ) {
      return;
   }

   //not implemented
   Double_t nphothreshold = gAnalyzer->GetXECEnePMWeightCorrection()->GetDeadChannelRecoveryNphoThreshold();
   for (auto pm : gamma->fvPM) {
      if (pm->fInvalid || pm->fPMRH->GetIsBad()) {

         // Int_t iPM = pm->fIndex;
         Double_t solidangle_surround = 0;
         Double_t npho_surround = 0;
         Int_t nsurround = 0;
         // Double_t npho_target = pm->fNpho;
         Double_t solidangle_target = GetSolidAngle(gamma->fXYZ, pm->fIndex);

         for (auto jpm : gamma->fvPM) {
            if (jpm->fPMRH->GetFace() != pm->fPMRH->GetFace() || jpm->fPMRH->GetIsBad()) {
               continue;
            }
            // if (TMath::Abs(pm->fPMRH->GetNumberInRow() - jpm->fPMRH->GetNumberInRow()) < 3 &&
            //     TMath::Abs(pm->fPMRH->GetRowInFace() - jpm->fPMRH->GetRowInFace()) < 3) {
            Double_t distance = TMath::Sqrt(TMath::Power(pm->fPMRH->GetXYZAt(0) - jpm->fPMRH->GetXYZAt(0), 2) +
                                            TMath::Power(pm->fPMRH->GetXYZAt(1) - jpm->fPMRH->GetXYZAt(1), 2) +
                                            TMath::Power(pm->fPMRH->GetXYZAt(2) - jpm->fPMRH->GetXYZAt(2), 2));
            if (distance < gAnalyzer->GetXECEnePMWeightCorrection()->GetDeadChannelRecoveryDistanceThreshold()) {
               Double_t solidangle = GetSolidAngle(gamma->fXYZ, jpm->fIndex);
               npho_surround += jpm->fNpho;
               solidangle_surround += solidangle;
               nsurround++;
            }
         }

         if (npho_surround > nphothreshold && gAnalyzer->GetXECEnePMWeightCorrection()->GetDeadChannelRecoveryFlag()==kRecoverFromSurroundingsSolidAngle) {
            pm->fNphoEstimated = npho_surround * solidangle_target / solidangle_surround;
            pm->fNphoEstimatedFlag = true;
         } else {
            pm->fNphoEstimated = npho_surround / nsurround;
            pm->fNphoEstimatedFlag = true;
         }
      }
   }

   Float_t factor;
   Float_t nface2Raw[kNumberOfFaces] = {0.};
   Float_t nface2Recovered[kNumberOfFaces] = {0.};
   for (auto pm : gamma->fvPM) {
      factor = pm->fPMRH->GetQsum2Weight() * pm->fSolidAngleCorr * pm->fFaceCorr;
      if (pm->fNpho >= 0) {
         nface2Raw[pm->fPMRH->GetFace()] += pm->fNpho * factor;
      }
      if (pm->fNphoEstimatedFlag) {
         if (pm->fNphoEstimated > 0) {
            nface2Recovered[pm->fPMRH->GetFace()] += pm->fNphoEstimated * factor;
         }
      } else {
         if (pm->fNpho >= 0) {
            nface2Recovered[pm->fPMRH->GetFace()] += pm->fNpho * factor;
         }
      }
   }
   for (auto pm : gamma->fvPM) {
      if (nface2Raw[pm->fPMRH->GetFace()] > 0 && nface2Recovered[pm->fPMRH->GetFace()] > 0) {
         pm->fDeadCorr = nface2Recovered[pm->fPMRH->GetFace()] / nface2Raw[pm->fPMRH->GetFace()];//constant enhancement
      } else {
         pm->fDeadCorr = 1.;
      }
   }
}

