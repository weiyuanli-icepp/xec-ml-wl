void MEGTXECEnePMWeight::Event()
{
   const Int_t nPM = gAnalyzer->GetXECRunHeader()->GetNPM();
   if (nPM <= 0) {
      return;
   }
   
   if (!EventSelection()) {
      return;
   }

   Release();

   Int_t nGamma;
   if (1) {
      //currently only one gamma is assumed.
      //if (gAnalyzer->GetXECPMAt(0)->Getfsize() > 1) {
      nGamma = 1;
   } else {
      nGamma = 0;
   }

   MEGXECEnePMWeightResult* pEnePMWeightResult;
   for (Int_t iPM = 0; iPM < nPM; iPM++) {
      pEnePMWeightResult = gAnalyzer->GetXECEnePMWeightResultAt(iPM);
      pEnePMWeightResult->SetweightSize(nGamma);
      pEnePMWeightResult->Setcharge2nphoSize(nGamma);
      pEnePMWeightResult->SetsolidangleSize(nGamma);
      pEnePMWeightResult->SetsolidanglecorrSize(nGamma);
      pEnePMWeightResult->SetfacecorrSize(nGamma);
      pEnePMWeightResult->SetdeadcorrSize(nGamma);
      pEnePMWeightResult->SetnphoSize(nGamma);
      pEnePMWeightResult->SetnphofitSize(nGamma);
      pEnePMWeightResult->SetnphorecoveredSize(nGamma);
   }

   MEGXECPMRunHeader* pmrh;
   for (Int_t iGamma = 0; iGamma < nGamma; iGamma++) {
      RecGamma* gamma = new RecGamma(iGamma, nPM);
      for (Int_t iPM = 0; iPM < nPM; iPM++) {
         pmrh = gAnalyzer->GetXECPMRunHeaderAt(iPM);
         gamma->fvPM[iPM] = new PM();
         gamma->fvPM[iPM]->fIndex = iPM;
         gamma->fvPM[iPM]->fPMRH = pmrh;
         gamma->fvPM[iPM]->fNpho = gAnalyzer->GetXECPMClusterAt(iPM)->GetnphoAt(iGamma);
         // std::cout<<iPM<<" Npho : "<<gamma->fvPM[iPM]->fNpho<<std::endl;
         gamma->fvPM[iPM]->fInvalid = gAnalyzer->GetXECPMClusterAt(iPM)->GetinvalidAt(iGamma);
         Double_t conv_factor = pmrh->GetGain() * pmrh->GetWDGain()
                                * pmrh->GetQE() * pmrh->GetCE()
                                * pmrh->GetCTAP() * MEG::eplus;
         if (!gamma->fvPM[iPM]->fInvalid && conv_factor != 0) {
            gamma->fvPM[iPM]->fCharge2Npho = 1. / conv_factor;
         }
      }
      gamma->fMaxPMInner = GetMaxPMInner(iGamma, gamma->fvPM);
      gamma->fEnergy = GetGammaEnergy(iGamma, gamma->fvPM);//rough estimation

      if (GetSP()->GetSolidAngleCorrection()->GetActive()) {
         CalcSolidAngleCorrection(iGamma, gamma);
      }

      if (GetSP()->GetFaceCorrection()->GetActive()) {
         CalcFaceCorrection(iGamma, gamma);
      }

      if (gAnalyzer->GetXECEnePMWeightCorrection()->GetDeadChannelRecoveryFlag()) {
         RecoverDeadChannel(iGamma, gamma);
      }

      fvRecGamma.push_back(gamma);
   }


   // fill the results to the folder
   for (auto gamma : fvRecGamma) {

      // calculate weight combining the corrections above
      CalcWeight(gamma);

      // fill results to folder
      FillResult(gamma);
   }

   if (GetSP()->GetMakeTemplate()->GetActive()) {
      if (fvRecGamma.size() > 0) {
         AddTemplate(fvRecGamma[0]);
      }
   }
}

