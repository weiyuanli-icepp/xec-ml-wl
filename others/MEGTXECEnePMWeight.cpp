//______________________________________________________________________________
Int_t MEGTXECEnePMWeight::GetMaxPMInner(const Int_t iGamma, std::vector<PM*>& vPM)
{
   Int_t maxPM = -1;
   if (gAnalyzer->GetXECFastRecResultSize() > iGamma) {
      maxPM = gAnalyzer->GetXECFastRecResultAt(iGamma)->Getinmaxcl();
   } else {
      Float_t maxNpho = 0;
      for (auto pm : vPM) {
         if (pm->fPMRH->GetFace() != XECCONSTANTS::kInnerFace) {
            continue;
         }
         if (pm->fInvalid) {
            continue;
         }
         if (maxNpho < pm->fNpho) {
            maxNpho = pm->fNpho;
            maxPM = pm->fIndex;
         }
      }
   }
   return maxPM;
}

//______________________________________________________________________________
Float_t MEGTXECEnePMWeight::GetGammaEnergy(const Int_t iGamma, std::vector<PM*>& vPM)
{
   Float_t energy = 0;
   if (gAnalyzer->GetXECFastRecResultSize() > iGamma) {
      energy = gAnalyzer->GetXECFastRecResultAt(iGamma)->Getenergy();
   } else {
      Float_t nsum2(0), ninner2(0);
      for (auto pm : vPM) {
         if (pm->fInvalid) {
            continue;
         }
         nsum2 += pm->fNpho;
         if (pm->fPMRH->GetFace() == XECCONSTANTS::kInnerFace) {
            ninner2 += pm->fNpho;
         }
      }
      // Is it OK to call the correction here??
      MEGXECEneTotalSumRecCorrection *pCorrection = gAnalyzer->GetXECEneTotalSumRecCorrection();
      MEGXECEneTotalSumRecScaleCorrection *pScaleCorrection;
      TString nsumType;
      for (Int_t iEne = 0; iEne < pCorrection->GetNEne(); iEne++) {
         pScaleCorrection = gAnalyzer->GetXECEneTotalSumRecScaleCorrectionAt(iEne);
         nsumType = pScaleCorrection->GetNsumType();
         nsumType.ToUpper();
         if (nsumType == "FASTREC" || nsumType == "ALL") {//<-- representative
            nsum2 = nsum2 + ninner2 * pScaleCorrection->GetInnerRatio();
            for (Int_t iDeg = 0; iDeg < pScaleCorrection->GetDegree(); iDeg++) {
               energy += TMath::Power(nsum2, iDeg) * pScaleCorrection->GetCoefAt(iDeg);
            }
            break;
         }
      }
   }

   return energy;
}

//______________________________________________________________________________
void MEGTXECEnePMWeight::PrepareTemplate()
{
   Int_t nUBin = GetSP()->GetMakeTemplate()->GetNUBin();
   Int_t nVBin = GetSP()->GetMakeTemplate()->GetNVBin();
   Int_t nWBin = GetSP()->GetMakeTemplate()->GetNWBin();

   std::vector<Float_t> vUBinEdge, vVBinEdge, vWBinEdge;
   TObjArray *binArray = GetSP()->GetMakeTemplate()->GetUBinEdge().Tokenize(",");
   for (Int_t i = 0; i < binArray->GetEntries(); i++) {
      TObjString *binStr = (TObjString*)binArray->At(i);
      vUBinEdge.push_back(binStr->GetString().Atof());
   }
   delete binArray;
   binArray = GetSP()->GetMakeTemplate()->GetVBinEdge().Tokenize(",");
   for (Int_t i = 0; i < binArray->GetEntries(); i++) {
      TObjString *binStr = (TObjString*)binArray->At(i);
      vVBinEdge.push_back(binStr->GetString().Atof());
   }
   delete binArray;
   binArray = GetSP()->GetMakeTemplate()->GetWBinEdge().Tokenize(",");
   for (Int_t i = 0; i < binArray->GetEntries(); i++) {
      TObjString *binStr = (TObjString*)binArray->At(i);
      vWBinEdge.push_back(binStr->GetString().Atof());
   }
   delete binArray;

   if (nUBin < 0) {
      nUBin = vUBinEdge.size() - 1;
   }
   if (nVBin < 0) {
      nVBin = vVBinEdge.size() - 1;
   }
   if (nWBin < 0) {
      nWBin = vWBinEdge.size() - 1;
   }

   if (nUBin != (Int_t)vUBinEdge.size() - 1) {
      const Float_t lowerEdge = vUBinEdge[0];
      const Float_t higherEdge = vUBinEdge[vUBinEdge.size() - 1];
      vUBinEdge.clear();
      for (Int_t iBin = 0; iBin <= nUBin; iBin++) {
         vUBinEdge.push_back(lowerEdge + (higherEdge - lowerEdge) / nUBin * iBin);
      }
   }
   if (nVBin != (Int_t)vVBinEdge.size() - 1) {
      const Float_t lowerEdge = vVBinEdge[0];
      const Float_t higherEdge = vVBinEdge[vVBinEdge.size() - 1];
      vVBinEdge.clear();
      for (Int_t iBin = 0; iBin <= nVBin; iBin++) {
         vVBinEdge.push_back(lowerEdge + (higherEdge - lowerEdge) / nVBin * iBin);
      }
   }
   if (nWBin != (Int_t)vWBinEdge.size() - 1) {
      const Float_t lowerEdge = vWBinEdge[0];
      const Float_t higherEdge = vWBinEdge[vWBinEdge.size() - 1];
      vWBinEdge.clear();
      for (Int_t iBin = 0; iBin <= nWBin; iBin++) {
         vWBinEdge.push_back(lowerEdge + (higherEdge - lowerEdge) / nWBin * iBin);
      }
   }

   const Int_t nPM = gAnalyzer->GetXECRunHeader()->GetNPM();
   fLDTemplateTable = new XECLDTemplateTable();
   XECLDTemplate_PMInfo* pmInfo = new XECLDTemplate_PMInfo(nPM);
   XECLDTemplate* tmpl;
   for (Int_t iUBin = 0; iUBin < nUBin; iUBin++) {
      for (Int_t iVBin = 0; iVBin < nVBin; iVBin++) {
         for (Int_t iWBin = 0; iWBin < nWBin; iWBin++) {
            tmpl = new XECLDTemplate(pmInfo);
            tmpl->SetIndex(fLDTemplateTable->GetSize());
            tmpl->SetU((vUBinEdge[iUBin] + vUBinEdge[iUBin + 1]) / 2);
            tmpl->SetV((vVBinEdge[iVBin] + vVBinEdge[iVBin + 1]) / 2);
            tmpl->SetW((vWBinEdge[iWBin] + vWBinEdge[iWBin + 1]) / 2);
            tmpl->SetURange(vUBinEdge[iUBin], vUBinEdge[iUBin + 1]);
            tmpl->SetVRange(vVBinEdge[iVBin], vVBinEdge[iVBin + 1]);
            tmpl->SetWRange(vWBinEdge[iWBin], vWBinEdge[iWBin + 1]);
            fLDTemplateTable->AddLDTemplate(tmpl);
         }
      }
   }
   fLDTemplateTable->SortTable();
}

//______________________________________________________________________________
void MEGTXECEnePMWeight::AddTemplate(MEGTXECEnePMWeight::RecGamma* gamma)
{
   GetPileupGamma();
   if (fvPLGamma.size() > 0) {
      return;
   }

   const Int_t iGamma = gamma->fIndex;
   if (!GetPosition(iGamma, gamma)) {
      Report(R_WARNING, "Position of %d-th gamma is not found. Solid angle correction is skipped.\n", iGamma);
      return;
   }

   XECLDTemplate* tmpl = fLDTemplateTable->GetLDTemplate(gamma->fUVW);
   if (!tmpl) {
      return;
   }

   Float_t nsum2(0), ninner2(0), npmt2(0);
   if (gAnalyzer->GetXECFastRecResultSize() > iGamma) {
      nsum2 = gAnalyzer->GetXECFastRecResultAt(iGamma)->Getnsum2();
      ninner2 = gAnalyzer->GetXECFastRecResultAt(iGamma)->Getninner2();
      npmt2 = gAnalyzer->GetXECFastRecResultAt(iGamma)->Getnpmt2();
   } else {
      for (auto pm : gamma->fvPM) {
         if (pm->fInvalid) {
            continue;
         }
         nsum2 += pm->fNpho;
         if (pm->fPMRH->GetFace() == XECCONSTANTS::kInnerFace) {
            ninner2 += pm->fNpho;
         } else {
            npmt2 += pm->fNpho;
         }
      }
   }

   const Int_t nPM = gAnalyzer->GetXECRunHeader()->GetNPM();
   std::vector<Float_t> npho(nPM, 0);
   for (Int_t iPM = 0; iPM < nPM; iPM++) {
      PM* pm = gamma->FindPM(iPM);
      if (pm && !pm->fInvalid) {
         // npho[iPM] = pm->fNpho / TMath::Max((Float_t)1e-12, pm->fPMRH->GetIsSiPM() ? ninner2 : npmt2);
         npho[iPM] = pm->fNpho;
      }
   }

   tmpl->AddEvent(npho);
   tmpl->AddMPPCR(ninner2 / nsum2);

   return;
}
